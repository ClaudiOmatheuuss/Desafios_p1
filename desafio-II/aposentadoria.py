# 9. Faça um programa que solicite ano de nascimento e imprima quando a pessoa vai se aposentar. Obs: Aposentadoria é com 65 anos completos.

ano_nascimento = int(input("Digite o ano de seu nascimento: "))

ano_aposentadoria = ano_nascimento + 65

print(f"você pode se aposentar apartir do ano de {ano_aposentadoria}")